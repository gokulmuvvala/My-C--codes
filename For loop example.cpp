// for loop example in c++ printing n numbers
#include<iostream>
 int main()
{
	int i,n; // Declaring the variables		
	std::cout<<"Enter the n value :"<<std::endl;
	std::cin>>n;	
	for(i=1;i<=n;i++)
	{
	std::cout<<i<<std::endl;	
	}
}
